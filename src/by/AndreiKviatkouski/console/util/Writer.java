package by.AndreiKviatkouski.console.util;

public class Writer {
    public static void writeString(String a) {
        System.out.println(a);
    }

    public static void writeInt(int a) {
        System.out.println(a);
    }

    public static void writeDouble(double a) {
        System.out.println(a);
    }

    public static void writeObject(Object message) {
        System.out.println(message);
    }
}
